public class LearnIfCondition {
public static void main(String[] args) {
	int number=0;
	
	if (number > 0) {
		System.out.println(number + " is postive number");
	}
	else if(number < 0) {
		System.out.println(number + " is negative number");
	}
	else {
		System.out.println(number + " is neutral number");
	}
	
	}
}
