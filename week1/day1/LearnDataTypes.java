package week1.day1;

public class LearnDataTypes {
	public static void main(String[] args) {
	
		//datatyp variable = value
		short age_one = 25;
		int age = 25;
		long phoneNumber = 9876543210L;
		float mileage = 24.6f;
		boolean isNew = true;
		String brandName = "Honda";
		char logo = 'H';
		double kms = 1500.6789;
		
		System.out.println(phoneNumber);
		System.out.println(mileage + " " + isNew);
		System.out.println("Age is : "+ age);
		System.out.println("Brand Name is : " + brandName);
		System.out.println(logo + " " + kms + " " + age_one);
	}
}
